    private void UgestionMisDBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UgestionMisDBActionPerformed
        String UgestAddUserNameVar = UgestAddUserName.getText();
        UgestAddUserName.setText("");
        String UgestAddMotPassVar = UgestAddMotPass.getText();
        UgestAddMotPass.setText("");
        String UgestAddNomPrenomVar = UgestAddNomPrenom.getText();
        UgestAddNomPrenom.setText("");
        String UseridVar = Userid.getText();
        String isAdminvar = (String) isMe.getSelectedItem(), BolAdmin = "";
        if (UseridVar.equals("1")) {
            JOptionPane.showMessageDialog(this, "Vous ne pouvez pas mettre à jour les informations de cet utilisateur");
        } else {
            if (isAdminvar.equals("Admin")) {//Semple Utlisateur
                BolAdmin = "true";
            } else {
                BolAdmin = "false";
            }
            if (UgestAddUserNameVar.equals("") || UgestAddMotPassVar.equals("") || UgestAddNomPrenomVar.equals("")) {
                JOptionPane.showMessageDialog(this, "le Nom et Prenom et Nom Utilisateur ,Mot de Pass ,est une obligation que vous devez remplir");
            } else {
                try {
                    String query = "UPDATE user "
                            + " SET  nomutilisateur = '" + UgestAddUserNameVar + "',password = '" + UgestAddMotPassVar + "' ,NomPrenom = '" + UgestAddNomPrenomVar + "' ,IsAdmin = '" + BolAdmin + "' "
                            + " WHERE Id = '" + UseridVar + "'";
                    pst = Conn.prepareStatement(query);
                    pst.executeUpdate();
                } catch (Exception e) {

                }
            }
            TableFilUser();
            Userid.setText("0");
        }


    }//GEN-LAST:event_UgestionMisDBActionPerformed

