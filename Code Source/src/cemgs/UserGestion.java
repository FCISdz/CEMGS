    private void UgestionAddDBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UgestionAddDBActionPerformed
        String UgestAddUserNameVar = UgestAddUserName.getText();
        String UgestAddMotPassVar = UgestAddMotPass.getText();
        String UgestAddNomPrenomVar = UgestAddNomPrenom.getText();
        String isAdminvar = (String) isMe.getSelectedItem(), BolAdmin = "";
        if (isAdminvar.equals("Admin")) {//Semple Utlisateur
            BolAdmin = "true";
        } else {
            BolAdmin = "false";
        }
        if (UgestAddUserNameVar.equals("") || UgestAddMotPassVar.equals("") || UgestAddNomPrenomVar.equals("")) {
            JOptionPane.showMessageDialog(this, "le Nom et Prenom et Nom Utilisateur ,Mot de Pass ,est une obligation que vous devez remplir");
        } else {
            try {
                String Sql = "insert into user (NomPrenom , nomutilisateur, password, IsAdmin) values ('" + UgestAddNomPrenomVar + "','" + UgestAddUserNameVar + "'"
                        + ",'" + UgestAddMotPassVar + "','" + BolAdmin + "')";
                pst = Conn.prepareStatement(Sql);
                pst.execute();
            } catch (Exception e) {
            }
        }
        TableFilUser();
        UgestAddUserName.setText("");
        UgestAddMotPass.setText("");
        UgestAddNomPrenom.setText("");
        Userid.setText("0");
    }//GEN-LAST:event_UgestionAddDBActionPerformed

